class planet:  # Create a class to store the properties of each planet
    def __init__(
        self,
        moon,
        mass,
        diameter,
        density,
        gravity,
        escape_velocity,
        rotation_period,
        length_day,
        avg_dist,
        perihelion,
        aphelion,
        T,
        v,
        i,
        e,
        o,
        Temp,
        P,
        num_moons,
        rings,
        mag_field,
    ):
        self.moon = moon
        self.mass = mass
        self.diameter = diameter
        self.radius = diameter / 2
        self.r = diameter / 2
        self.density = density
        self.gravity = gravity
        self.escape_velocity = escape_velocity
        self.rotation_period = rotation_period
        self.length_day = length_day
        self.avg_dist = avg_dist
        self.perihelion = perihelion
        self.aphelion = aphelion
        self.T = T
        self.v = v
        self.i = i
        self.e = e
        self.o = o
        self.Temp = Temp
        self.P = P
        self.num_moons = num_moons
        self.rings = rings
        self.mag_field = mag_field


